package iLoops;

public class CancionElefantes {

    public static void main(String[] args) {
        int numeroElefantes = 1;
        while (numeroElefantes <=100){  //ciclo
            if (numeroElefantes == 1) {
                System.out.println(numeroElefantes + " elefante se columpiaba sobre la tela de una araña, fue a llamar otro elefante");
            }
                else {
                System.out.println(numeroElefantes + " elefantes se columpiaba sobre la tela de una araña, fueron a llamar otro elefante");
            }
                numeroElefantes ++;
        }

        //instrucciones
        //actualizar el contador
    }
}